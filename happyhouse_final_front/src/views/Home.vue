<script>
function isElementUnderBottom(elem, triggerDiff) {
  const { top } = elem.getBoundingClientRect();
  const { innerHeight } = window;
  return top > innerHeight + (triggerDiff || 0);
}

function isElementOverBottom(elem) {
  const { top } = elem.getBoundingClientRect();
  const { innerHeight } = window;
  return innerHeight > top+600;
}


function handleScroll() {
  const elems = document.querySelectorAll('.up-on-scroll');
  const elems2 = document.querySelectorAll('.wipe-on');
  const elems3 = document.querySelectorAll('.wipe-left');
  const elems4 = document.querySelectorAll('.fade-in');
  elems.forEach(elem => {
    if (isElementUnderBottom(elem, -20)) {
      elem.style.opacity = "0";
      elem.style.transform = 'translateY(70px)';
    } 
    else if(isElementOverBottom(elem)) {
      elem.style.opacity = "0";
      elem.style.transform = 'translateY(-70px)';
    }
    else {
      elem.style.opacity = "1";
      elem.style.transform = 'translateY(0px)';
    }
  })

  elems2.forEach(elem => {
    if (isElementUnderBottom(elem, -20)) {
      elem.style.opacity = "0";
      elem.style.transform = 'translateY(70px)';
    } else {
      elem.style.opacity = "1";
      elem.style.transform = 'translateY(0px)';
    }
  })

  elems3.forEach(elem => {
    if (isElementUnderBottom(elem, -20)) {
      elem.style.opacity = "0";
      elem.style.transform = 'translateX(70px)';
    } 
    else if(isElementOverBottom(elem)) {
      elem.style.opacity = "0";
      elem.style.transform = 'translateX(-70px)';
    }
    else {
      elem.style.opacity = "1";
      elem.style.transform = 'translateY(0px)';
    }
  })
  elems4.forEach(elem => {
    if (isElementUnderBottom(elem, -20)) {
      elem.style.opacity = "0";
    } else {
      elem.style.opacity = "1";
    }
  })
}

window.addEventListener('scroll', handleScroll);

</script>
